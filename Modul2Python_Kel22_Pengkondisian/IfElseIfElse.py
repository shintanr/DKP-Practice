angka = float (input ('Masukkan angka yang anda inginkan :'))


#if-else if-else untuk cek merupakan bilangan ganjil atau genap


if angka % 2 == 0 :
    print ('Angka yang anda masukkan bilangan genap yaitu %s' %angka)
elif angka % 2 == 1 :
    print ('Angka yang anda masukkan merupakan bilangan ganjil yaitu %s' %angka)
else :
    print ('Angka yang anda masukkan adalah bilangan bulat yaitu %s ' %angka)